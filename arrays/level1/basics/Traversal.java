package arrays.level1.basics;

public abstract class Traversal {

    /*
     * Variant: Traversal & Extremes - single linear scans to find or aggregate values.
     * [A01] findMax       ☐
     * [A02] secondLargest  ☐
     * [A03] minAndMax     ☐
     */

    public abstract int findMax(int[] arr);

    public abstract int secondLargest(int[] arr);

    public abstract int[] minAndMax(int[] arr);

}
