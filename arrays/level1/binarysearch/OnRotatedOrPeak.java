package arrays.level1.binarysearch;

public abstract class OnRotatedOrPeak {

    /*
     * Variant: On rotated / unimodal arrays - decide which half is sorted, or climb to a peak.
     * [H04] searchRotated  ☐
     * [H05] findMinRotated  ☐
     * [H06] findPeak       ☐
     */

    public abstract int searchRotated(int[] arr, int target);

    public abstract int findMinRotated(int[] arr);

    public abstract int findPeak(int[] arr);

}
