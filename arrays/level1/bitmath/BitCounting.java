package arrays.level1.bitmath;

public abstract class BitCounting {

    /*
     * Variant: Bit counting / manipulation - per-bit reasoning across numbers.
     * [J04] countBits       ☐
     * [J05] hammingDistance  ☐
     * [J06] reverseBits     ☐
     */

    public abstract int[] countBits(int n);

    public abstract int hammingDistance(int x, int y);

    public abstract int reverseBits(int x);

}
