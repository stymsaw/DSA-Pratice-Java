package arrays.level1.hashing;

public abstract class FrequencyMap {

    /*
     * Variant: Frequency map - count occurrences, then reason over the counts.
     * [G01] majorityElement    ☐
     * [G02] firstUniqueIndex   ☐
     * [G03] countPairsWithDiff  ☐
     */

    public abstract int majorityElement(int[] arr);

    public abstract int firstUniqueIndex(int[] arr);

    public abstract int countPairsWithDiff(int[] arr, int diff);

}
