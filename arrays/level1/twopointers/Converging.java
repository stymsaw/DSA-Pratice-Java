package arrays.level1.twopointers;

public abstract class Converging {

    /*
     * Variant: Converging (opposite ends) - start and end pointers move toward each other.
     * [B01] isPalindrome  ☐
     * [B02] twoSumSorted  ☐
     * [B03] maxArea      ☐
     */

    public abstract boolean isPalindrome(int[] arr);

    public abstract int[] twoSumSorted(int[] arr, int target);

    public abstract int maxArea(int[] height);

}
