package arrays.level1.twopointers;

public abstract class SameDirection {

    /*
     * Variant: Same-direction (slow/fast writer pointer) - one pointer reads, one writes.
     * [B04] removeElement    ☐
     * [B05] removeDuplicates  ☐
     * [B06] moveZerosStable  ☐
     */

    public abstract int removeElement(int[] arr, int val);

    public abstract int removeDuplicates(int[] arr);

    public abstract void moveZerosStable(int[] arr);

}
